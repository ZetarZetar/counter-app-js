// Increment Number value
export const IncreaseNumber = (currentValue) => {
  currentValue++;
  return currentValue;
};

// Decrement Number value
export const DecreaseNumber = (currentValue) => {
  currentValue--;
  return currentValue;
};

// Reset Number value
export const ResetNumber = (currentValue) => {
  currentValue = 0;
  return currentValue;
};
